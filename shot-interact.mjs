import { chromium } from 'playwright';
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 1440, height: 900 } });
const errs = [];
page.on('pageerror', e => errs.push(String(e)));
await page.goto('http://localhost:3000/build-program.html', { waitUntil: 'networkidle' });
await page.click('[data-comp="bld"]');
await page.click('[data-portion="small"]');
await page.click('[data-dur="7"]');
const small = await page.textContent('#sum-total');
await page.click('[data-portion="large"]');
const large = await page.textContent('#sum-total');
const portionShown = await page.textContent('#sum-portion');
// Dinner Only + medium + 40 => $2,146
await page.click('[data-comp="dinner"]');
await page.click('[data-portion="medium"]');
await page.click('[data-dur="40"]');
const dinner = await page.textContent('#sum-total');
console.log('Small(BLD,7d):', small, '| Large(BLD,7d):', large, '| portion label:', portionShown, '| Dinner medium 40d:', dinner);
console.log('JS errors:', errs.length ? errs : 'none');
await page.screenshot({ path: './screenshots/build-program-portion.png', fullPage: true });
await browser.close();
